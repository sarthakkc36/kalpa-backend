    <script>
        function animateStats() {
            const stats = document.querySelectorAll('.about-stat-number');
            
            stats.forEach(stat => {
                const targetValue = parseInt(stat.getAttribute('data-value'));
                const duration = 2000; // Animation duration in milliseconds
                const steps = 60; // Number of steps in animation
                const stepValue = targetValue / steps;
                let currentValue = 0;
                let currentStep = 0;
                const hasPlus = stat.classList.contains('plus');
        
                const interval = setInterval(() => {
                    currentStep++;
                    currentValue = Math.min(Math.round(stepValue * currentStep), targetValue);
                    stat.textContent = currentValue.toLocaleString() + (hasPlus ? '+' : '');
        
                    if (currentStep >= steps) {
                        clearInterval(interval);
                        stat.textContent = targetValue.toLocaleString() + (hasPlus ? '+' : '');
                    }
                }, duration / steps);
            });
        }
        
        // Trigger animation when section is in view
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateStats();
                    observer.unobserve(entry.target);
                }
            });
        }, { threshold: 0.5 });
        
        document.addEventListener('DOMContentLoaded', () => {
            const statsSection = document.querySelector('.about-stats');
            if (statsSection) {
                observer.observe(statsSection);
            }
        });
        </script>
